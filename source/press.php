<h1>$25,000 Prize for Free/Open Source Software Development</h1>

<p class="pr-summary">
Up to $25,000 will go to developers who add voice and video chat
to Pidgin, a popular instant messaging client released under the GNU
General Public License (GPL).  The prize is being offered to promote a
new bounty-based free/open source software (FOSS) funding website.
</p>

<p>
<b>LaSalle, Ontario (PRWEB) April 9, 2008</b> --
A new web start-up has announced a prize of up to $25,000 CAD for the
addition of voice and video chat to Pidgin, a free instant messaging
client.  Through their new free software funding site, FOSS Factory
Inc. (http://www.fossfactory.org/) has committed $5,000 CAD, and promised
another $10,000 CAD to match contributions from any other contributors.
</p>

<p>
Pidgin, a program that lets you talk to your friends on MSN, Yahoo!,
Google Talk, and many other systems all at the same time, has gained a
wide, loyal user base thanks to its generous licensing terms, and its
professional quality and usability.  But its users have been wishing for
voice and video support for several years.  Partly due to the difficulty
of implementing this feature, Pidgin's FAQ states: "It could be tomorrow,
or it could be in 2099".
</p>

<p>
"Pidgin voice and video is a perfect example of a free software project
that could use a boost", said John-Paul Gignac, founder of FOSS Factory,
and a successful entrepreneur and free software enthusiast.  "There's lots
of demand for the feature, but until now, there's been no way to translate
that demand into solid development funds."
</p>

<p>
FOSS Factory's new system lets participants split bounties into tiny
sub-projects.  That way, developers can collaborate without worrying
about how the money will be divided, and they can get paid as they go.
There is no need for project bidding, or exclusive developer selection:
Anyone can participate as their time permits.
</p>

<p>
"Compared to other FOSS funding techniques, we help dedicated FOSS
users contribute funds in a more effective, meaningful way", said
Abdullah Bakhach, FOSS Factory's director of business development.
"Our monthly sponsorship system can generate a much-needed revenue stream
for highly-voted projects."
</p>

<p>
FOSS Factory Inc. is offering this prize as a way of introducing their
website to the FOSS community.  They are appealing to Pidgin's users to
help bring the bounty to the full $25,000.
</p>

<p>
FOSS Factory charges $10 for posting a new project on their site, or
$15/month for unlimited project posting.  But they don't get a dime
from project sponsorships.  "We feel it's very important to keep our
hands out of the development phase," said John-Paul Gignac.  "The site
is intended as a service to the community, so we don't want to interfere
with that aspect."
</p>

