necessary and permitted under law.
</p>

<p>
If your personal information as held by FOSS Factory is incorrect or
incomplete, you may provide us with amendments to ensure the accuracy
and completeness of the information. FOSS Factory will update our records
within 30 days of receipt.
</p>

<a name="retention"></a>
<h2>Retention of Personal Information</h2>
<p>
FOSS Factory is required by law to retain personal information only for
as long as necessary to fulfil the purpose for which it was collected.
</p>

<p>
The personal information of users is retained by FOSS Factory for as
long as the user has a FOSS Factory account. Account information is
further retained for one fiscal year after the termination of a FOSS
Factory account for any reason. This is to ensure compatibility with
the schedules of any credit agencies that may be involved in billing
for the terminated account.
</p>

<p>
Certain personal information is retained by FOSS Factory
indefinitely. FOSS Factory retains evidence of your licensing of your
works in order to ensure the integrity of that licence in the future, for
the life of your copyright. This includes version information related to
each document on the site, including the user who made each modification
to the document. You maintain ownership of the copyright in all of your
own works that you submit to FOSS Factory, and you provide a licence for
the use of those works. Therefore, FOSS Factory retains the real name
and username of each account, even after termination of accounts. Since
