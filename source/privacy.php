<a name="cookies"></a>
<h2>Cookies</h2>
<p>
In order to use the FOSS Factory site your browser must be set to accept
cookies. As you browse the FOSS Factory site, cookies may be placed
on your computer's hard drive that store your username, password,
viewing preference, authentication, and interface preferences. This is
done to match your information with your activities so you do not need
to retype the information each time you wish to view a new page. Cookies are
also used to keep track of your preferences, from one visit to the next.
</p>

<p>
Cookies may be used to gather usage information, such as the frequency of
visitors on the site and its pages, and the date and time of access. This
data may be analyzed to understand which portions of the site our
customers use, and to improve the site's functionality and features.
</p>
 
