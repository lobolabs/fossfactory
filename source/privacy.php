<a name="purpose"></a>
<h2>Purpose of Collection</h2>
<p>
When you register with FOSS Factory, your personal information will be
required to verify membership eligibility, process membership orders,
and to communicate with you about your membership. FOSS Factory also
uses your e-mail address to send you newsletters and updates about FOSS
Factory's services, including changes to our Terms and Conditions and
Privacy Policy.
</p>

<p>
Occasionally, users may receive special offers and other promotional
information, from FOSS Factory or third-parties, through e-mail. Your
personal information will not be disclosed to any third-parties for
marketing purposes without your expressed consent. When FOSS Factory
contacts you on behalf of a third party, at no time will they see your
e-mail address or any other information that could identify you or be
used to contact you directly. You may choose to remove yourself from
these lists permanently by modifying your account preferences.
</p>

<p>
FOSS Factory records the date and time of the membership transaction and
creates a unique membership number that is attributed to your account
with FOSS Factory. FOSS Factory will also request that you select a
username and password for your account. Representatives of FOSS Factory
will never ask for your password, and you should never disclose your
password to anyone. Your user and billing information can be accessed
in your account preferences and can be corrected or updated.
</p>

