<h1>Introduction</h1>

<p>
The founding team of FOSS Factory has a history of successful business
start-ups in the IT industry.  Steve Sutherland, the lead investor,
met and hired John-Paul, the current president, back in 1994 to help
build TrueSpectra, an internet imaging and dynamic composition software
company.  TrueSpectra was rebranded as Scene7 and later bought out by
Adobe in 2007.
</p>

<p>
Steve and John-Paul left TrueSpectra in 1999 and immediately co-founded
OpenGraphics, a start-up that provides solutions for internet photo
sharing, composition, print fulfillment, and wireless access services.
OpenGraphics rapidly grew in operation and was soon acquired by Best
Buy in 2003 and rebranded as Siberra.  Steve and John Paul continued to
hold senior positions at Siberra where they met Abdullah Bakhach in 2005.
</p>

<p>
After leaving Siberra, John-Paul and Abdullah started work on the vision
of FOSS Factory, with John-Paul as lead developer and Abdullah in the
role of business development.  Work on FOSS Factory progressed, and
Steve Sutherland soon became its lead investor and advisor.
</p>

<hr size="1">
<h1>The Team</h1>
<h1>John-Paul Gignac, BMath</h1>
<h2>Founder</h2>
<img src="images/jp.jpg">

<p>
John-Paul is an inventor and a successful entrepreneur. A graduate of
Math and Computer Science from the University of Waterloo, John-Paul is
especially skilled in software architecture, computer graphics, digital
imaging and web programming.  At TrueSpectra JP shone as lead developer,
finding unorthodox solutions to complex problems. At OpenGraphics,
JP provided the necessary web software development expertise to help achieve
Steve's vision.  After OpenGraphics was sold, John-Paul continued in
the role of Senior Software Architect until mid-2006.
</p>

<p>
In 1995, while working at TrueSpectra, JP decided to test the hype of
free/open source software by having a friend install GNU/Linux on a
spare partition on his work machine.  The ease and familiarity of the
new system converted JP immediately and he never looked back.
</p>

<p>
JP became an avid user of FOSS, using its software at work and at home.
As a FOSS enthusiast, JP made several software contributions to the FOSS
community.  Besides FOSS Factory, his most prominent contribution was
leading an international team of developers and contributors to create
Pathological, a puzzle game that is available on most major Linux
distributions.
</p>

<p>
JP's deep understanding of the FOSS community framework combined with his
innovative thinking, particularly in the field of game theory, helped him
come up with the creative design behind FOSS Factory. His appreciation of
the importance of the open and collaborative spirit of the FOSS community
helped him avoid the pitfalls of previous failed bounty model attempts.
</p>

<p>
John-Paul is also co-founder of Crosswing, Inc., a research start-up
working to produce consumer robots.
</p>

<p>
When not working on FOSS Factory and Crosswing, John-Paul loves to play
Nintendo Wii games with his lovely wife, Kim, and his four beautiful children.
</p>

<hr size="1">

<h1>Steve Sutherland, BASc</h1>
<h2>Lead investor, Advisor</h2>
<img src="images/steve2.jpg">

<p>
With three decades of innovation experience, Steve has founded firms
delivering cutting edge products in a number of markets including
telecommunications, electronic publishing, document management,
graphic design, digital fulfillment networking, wireless imaging,
internet content licensing, instant visual communications, robotics, and
artificial intelligence. In addition to executing industry partnerships
with firms including IBM, Kodak, Bell, and HP, Steve has seen his teams'
innovations and related patent portfolios acquired by industry leaders
seeking to grow their markets - most recently Best Buy's acquisition of
OpenGraphics and Adobe's acquisition of Scene7, formerly TrueSpectra.
</p>

<p>
Steve's experience spans global industry initiatives including JPEG2000,
IIP (the Internet Imaging Protocol, IPP (the Internet Printing Protocol),
and CPXe (the Common Picture eXchange) - with Steve also having been
elected as Chairperson of IIP in the late '90's and CPXe in the early
2000's. Other past board memberships include the Digital Imaging Group
(now the International Imaging Industry Association), the Software and
Information Industry Association (Canadian Division), and the Consumer
Electronics Association (DI Board) charged with defining a new CEA
Division in 2006.
</p>

<p>
Many of Steve's initiatives have both leveraged and contributed to
the FOSS community since originally moving key development at
TrueSpectra in 1995 to GNU/Linux. In 2001 Steve adopted Debian and
PostgreSQL to drive OpenGraphics' server-side products. At FOSS Factory,
Steve has invested in the initial site infrastructure and provides
ongoing strategic guidance.
</p>

<p>
Steve graduated from the University of Waterloo with a BASc in Systems
Design Engineering.  Beyond professional activities, with his wife and
two children he enjoys boating, skiing, and travel.
</p>

