<h1>Abdullah Bakhach, BMath</h1>
<h2>President and Co-Founder</h2>
<img src="images/abdullah.jpg">

<p>
A winner of the 2006 Impact Innovator of the Year award, Abdullah Bakhach is a passionate student of entrepreneurship and
innovation.  He started his education at the University of Waterloo
in computer science, but soon moved to math and business to realize
his ambitions.  His first web design project was in 1999.
</p>

<p>
He is no stranger to start-ups.  Four out of his six co-op terms were
spent working at web design and e-commerce start-up companies.  His first
experience with a web start-up was working for arbaco.com, where he
was the lead designer and developer of zandirugs.com, a persian rugs
e-commerce site.
</p>

<p>
Abdullah met John Paul in his co-op term at Best Buy.  Abdullah was a
project lead for an online digital scrapbook creation tool and worked
closely with John-Paul on the project.  Abdullah's management and
leadership skills, as well as his initiative prompted JP to approach
him with the FOSS Factory idea.  Abdullah soon realized the potential
of FOSS Factory and started working for John Paul on building the site
during his following co-op term.
</p>

<p>
Abdullah works on developing the business and marketing strategy
of the company, as well as designing the look and feel of the site.
He constantly seeks advice and input about FOSS Factory from his former
professors, software developers, entrepreneurs, and academics worldwide.
Abdullah works to ensure that the principles of FOSS Factory are in
concert with the latest theories pertaining to internet sociology.
</p>

<p>
Abdullah enjoys reading books on a variety of topics that relate to
human behaviour at an individual and collective level.
</p>
